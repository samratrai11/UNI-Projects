library(gamlss)
data(grip)
set.seed(1143)
index<-sample(3766,1000)
mydata<-grip[index,]
dim(mydata)
plot(grip~age,data=mydata,col='maroon')
abccg<-gamlss(grip~pb(age),sigma.fo=~pb(age),nu.fo=~pb(age),data=mydata,family=BCCG)
edfAll(abccg)
abct<-gamlss(grip~pb(age),sigma.fo=~pb(age),nu.fo=~pb(age),tau.fo=~pb(age),data=mydata,family=BCT,start.from=abccg)
edfAll(abct)
abcpe<-gamlss(grip~pb(age),sigma.fo=~pb(age),nu.fo=~pb(age),tau.fo=~pb(age),data=mydata,family=BCPE,start.from=abccg)
edfAll(abcpe)
GAIC(abccg,abct,abcpe)
fittedPlot(abccg,abct,abcpe,x=mydata$age)
centiles(abccg)
centiles(abcpe)
centiles(abct)
centiles.split(abct)
plot(abccg)
plot(abct)
plot(abcpe)
wp(abccg)
wp(abct)
wp(abcpe)

